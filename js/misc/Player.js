function Player(name, airborne, noLasers, direction, health, lives, laserSpeed, speed) {

    this.name = name;
    this.airborne = airborne;
    this.noLasers = noLasers;
    this.direction = direction;
    this.health = health;
    this.lives = lives;
    this.laserSpeed = laserSpeed;
    this.speed = speed;
}